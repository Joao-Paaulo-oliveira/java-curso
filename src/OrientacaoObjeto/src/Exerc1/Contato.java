package Exerc1;

import java.util.Arrays;

public class Contato {

	String nome;
	String endereço;
	String email;
	String[] telefone;

	public String toString() {
		return "Contato [nome=" + nome + ", endereço=" + endereço + ", email=" + email + ", telefone="
				+ Arrays.toString(telefone) + "]";
	}
	
	
	
	
}
